// ==================== 工具函数 ====================

// 按来源分类
export const getChineseStories = () => classicStories.filter(s => s.origin === '中国');
export const getForeignStories = () => classicStories.filter(s => s.origin === '外国');

// 按分类获取
export const getStoriesByCategory = (category: string) =>
  classicStories.filter(s => s.category === category);

// 按年龄段获取
export const getStoriesByAgeGroup = (ageGroup: string) =>
  classicStories.filter(s => s.ageGroup === ageGroup);

// 按长度获取
export const getStoriesByLength = (length: 'short' | 'medium' | 'long') =>
  classicStories.filter(s => s.length === length);

// 按难度获取
export const getStoriesByDifficulty = (difficulty: 'easy' | 'medium' | 'hard') =>
  classicStories.filter(s => s.difficulty === difficulty);

// 获取所有分类
export const getAllStoryCategories = () =>
  Array.from(new Set(classicStories.map(s => s.category)));

// 获取所有国家
export const getAllCountries = () =>
  Array.from(new Set(classicStories.map(s => s.country).filter(Boolean)));

// 随机获取一个故事
export const getRandomStory = () =>
  classicStories[Math.floor(Math.random() * classicStories.length)];

// 根据关键词搜索故事
export const searchStories = (keyword: string) =>
  classicStories.filter(s =>
    s.title.includes(keyword) ||
    s.summary.includes(keyword) ||
    s.keywords.some(k => k.includes(keyword))
  );
